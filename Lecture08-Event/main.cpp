/*
================================================================================
 [강의: C 스타일 함수 포인터를 이용한 버튼 이벤트 처리]

 1. 핵심 개념:
    - 함수 포인터: 함수의 주소를 저장하는 변수.
    - 콜백(Callback): 특정 조건(클릭)이 만족되면 등록된 함수를 거꾸로 호출함.

 2. 왜 이렇게 하는가?
    - 버튼 코드를 한 번 짜놓으면, '게임 시작', '종료', '옵션' 등
      서로 다른 기능을 가진 버튼을 만들 때 버튼 내부 코드를 수정할 필요가 없음.
    - "행위"를 변수처럼 취급하는 법을 배우는 단계.

 3. 주의 사항:
    - 함수의 시그니처(반환형과 매개변수)가 일치해야 주소를 담을 수 있음.
================================================================================
*/

#include <stdio.h>
#include <conio.h>

// 1. 이벤트가 발생했을 때 호출될 함수의 규격(Type)을 정의함
// 리턴값 없고, 매개변수 없는 함수들만 이 버튼에 등록 가능함
typedef void (*OnClickHandler)();

// 2. 버튼 구조체 (구식 C 스타일)
struct Button
{
    const char* label;      // 버튼 이름
    int x, y, w, h;         // 버튼 영역
    OnClickHandler onClick; // 클릭되었을 때 실행할 함수의 '주소'를 담는 변수
};

// 3. 실제 버튼이 눌렸을 때 실행될 '기능'들 (함수들)
void StartGame()
{
    printf("[시스템] 게임을 시작합니다!\n");
}

void ExitGame()
{
    printf("[시스템] 프로그램을 종료합니다...\n");
}

// 4. 버튼을 실행하는 가상의 함수
void ClickButton(Button* btn)
{
    printf("[%s] 버튼을 클릭했습니다.\n", btn->label);

    // 만약 이 버튼에 등록된 함수 주소가 있다면 실행함
    if (btn->onClick != NULL)
    {
        btn->onClick(); // 등록된 주소로 점프해서 함수 실행!
    }
}

int main()
{
    // 5. 버튼 객체 생성 및 기능 연결 (함수 주소 대입)
    Button btn1 = { "START", 10, 10, 100, 50, StartGame };
    Button btn2 = { "EXIT",  10, 70, 100, 50, ExitGame };

    // 가상의 클릭 이벤트 발생
    _getch();
    ClickButton(&btn1);
    _getch();
    ClickButton(&btn2);

    return 0;
}